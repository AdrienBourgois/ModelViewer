void GUIEnvironment::ImGui_NewFrame()
{
	if (!g_FontTexture)
		ImGui_ImplSdlGL3_CreateDeviceObjects();

	ImGuiIO& io = ImGui::GetIO();

	int w, h;
	SDL_GetWindowSize(g_Window, &w, &h);
	io.DisplaySize = ImVec2((float)w, (float)h);
	io.DisplayFramebufferScale = ImVec2(1.0f, 1.0f);

	int mx, my;
	Uint32 mouseMask = SDL_GetMouseState(&mx, &my);
	if (SDL_GetWindowFlags(g_Window) & SDL_WINDOW_MOUSE_FOCUS)
		io.MousePos = ImVec2((float)mx, (float)my);
	else
		io.MousePos = ImVec2(-1, -1);

	io.MouseDown[0] = g_MousePressed[0] || (mouseMask & SDL_BUTTON(SDL_BUTTON_LEFT)) != 0;
	io.MouseDown[1] = g_MousePressed[1] || (mouseMask & SDL_BUTTON(SDL_BUTTON_RIGHT)) != 0;
	io.MouseDown[2] = g_MousePressed[2] || (mouseMask & SDL_BUTTON(SDL_BUTTON_MIDDLE)) != 0;
	g_MousePressed[0] = g_MousePressed[1] = g_MousePressed[2] = false;

	io.MouseWheel = g_MouseWheel;
	g_MouseWheel = 0.0f;

	SDL_ShowCursor(io.MouseDrawCursor ? 0 : 1);

	ImGui::NewFrame();
}

void GUIEnvironment::showGUI()
{
    bool show_stats_window = true;
    bool show_transformation_window = true;
    bool show_open_window = true;

    if (show_stats_window)
    {
        ImGui::SetNextWindowSize(ImVec2(200,100), ImGuiSetCond_FirstUseEver);
        ImGui::Begin("Informations", &show_stats_window);
        ImGui::Text("Nombre de sommets : %i", system->getMesh()->getN_data());
        ImGui::Text("Nombre de triangles : %i", system->getMesh()->getN_data()/3);
        ImGui::End();
    }

    if (show_transformation_window)
    {
        ImGui::SetNextWindowSize(ImVec2(400,500), ImGuiSetCond_FirstUseEver);
        ImGui::Begin("Transformation", &show_stats_window);
        ImGui::SliderFloat("Angle X", this->system->getDriver()->getAngleX(), 0.f, 360.f);
        ImGui::SliderFloat("Angle Y", this->system->getDriver()->getAngleY(), 0.f, 360.f);
        ImGui::SliderFloat("Angle Z", this->system->getDriver()->getAngleZ(), 0.f, 360.f);
        ImGui::End();
    }

    if (show_open_window)
    {
        char type = 0;
        int space = 0;

        ImGui::SetNextWindowSize(ImVec2(300,600), ImGuiSetCond_FirstUseEver);
        ImGui::Begin("Open",&show_open_window);

        if (open_window_update)
        {
            std::string path = this->files.getPath();
            strcpy(this->pathStr, path.c_str());
            this->list = this->files.getList();
            open_window_update = false;
        }

        for (unsigned int i = 1; i < files.getListFolder().size(); ++i)
        {
            space = i;
            ImGui::TextColored(ImVec4(1.0f,1.0f,0.0f,1.0f), "%*s-> %s", space*4, "", files.getListFolder(i).c_str());
        }

        for(unsigned int i = 1; i < this->list.size(); ++i)
        {
            type = this->list[i]->d_type;
            if (type == 4)
                ImGui::TextColored(ImVec4(1.0f,0.0f,1.0f,1.0f), "%*s%s", space*4, "", this->list[i]->d_name);
        }
        for(unsigned int i = 0; i < this->list.size(); ++i)
        {
            type = this->list[i]->d_type;
            if (type == 8)
                ImGui::Text("%*s%s", space*4, "", this->list[i]->d_name);
        }

        ImGui::InputText("Path", pathStr, 100);
        ImGui::SameLine();
        if(ImGui::Button("Valid"))
        {
            this->files.setPath((std::string)pathStr);
            open_window_update = true;
        }

        if(ImGui::Button("Open"))
        {
            delete this->system->getMesh();
            MeshObj* mesh = new MeshObj((std::string)pathStr, nullptr);
            this->system->setMesh(mesh);
        }

        ImGui::End();
    }
}

